package Arrays;

public class Integers {
	public static void main(String[] args) {
		int[] arr=new int[4];
		arr[0]=10;
		arr[1]=20;
		arr[2]=30;
		arr[3]=40;
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i]+"  ");
			
		}
		System.out.print("\n**************************************\n");
		int[]abb={10,20,30,40};
		for (int i = 0; i < abb.length; i++) {
			System.out.print(abb[i]+"   ");
			
		}
	}

}
