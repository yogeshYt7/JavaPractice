package Arrays;

public class Char {
	public static void main(String[] args) {
		char[] arr=new char[4];
		arr[0]='a';
		arr[1]='B';
		arr[2]='c';
		arr[3]='D';
		for (int i = 0; i < arr.length; i++) {
			System.out.print(arr[i]+"  ");
			
		}
				
		
		System.out.print("\n**************************************\n"); 
		
		
		
		
		char[]abb={'A','B','C','D'};
		for (char i = 0; i < abb.length; i++) {
			System.out.print(abb[i]+"   ");
			
		}
	}

}
