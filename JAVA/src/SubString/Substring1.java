package SubString;

public class Substring1 {
	public static void main(String[] args) {
		String str="Programming Language";
		System.out.println(str.substring(0,4));
		System.out.println(str.substring(4,8));
		System.out.println(str.substring(8,11));
		System.out.println(str.substring(12,16));
		System.out.println(str.substring(16));
		System.out.println(str.substring(0,4)+" "+str.substring(4,8)+" "+str.substring(8,11)+" "+str.substring(12,16)+" "+str.substring(16));
	}

}
